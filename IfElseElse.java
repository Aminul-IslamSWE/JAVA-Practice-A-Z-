package Statement;

public class IfElseElse {
    public static void main(String[] args) {
        int a = -5;

        if (a > 0) {
            System.out.println("The number is positive");
        } else if (a < 0) {
            System.out.println("Number is Negative");
        } else {
            System.out.println("Number is zero");
        }
    }
}
